<?php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Categorie
 *
 * @ORM\Table(name="categorie")
 * @ORM\Entity
 */
class Categorie
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string", length=255)
     */
    private $name;

    /**
     * @var string
     *
     * @ORM\Column(name="slug", type="string", length=255)
     */
    private $slug;

    /**
     * @var string
     *
     * @ORM\Column(name="description", type="text", nullable=true)
     */
    private $description;

    /**
     * @var Courrier
     *
     * @ORM\OneToMany(targetEntity="Courrier", mappedBy="categorie")
     */
    private $courriers;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return Categorie
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set slug
     *
     * @param string $slug
     * @return Categorie
     */
    public function setSlug($slug)
    {
        $this->slug = $slug;

        return $this;
    }

    /**
     * Get slug
     *
     * @return string
     */
    public function getSlug()
    {
        return $this->slug;
    }
    /**
     * Constructor
     */
    public function __construct()
    {
        $this->courriers = new \Doctrine\Common\Collections\ArrayCollection();
    }

    /**
     * Add courriers
     *
     * @param \AppBundle\Entity\Courrier $courriers
     * @return Categorie
     */
    public function addCourrier(\AppBundle\Entity\Courrier $courriers)
    {
        $this->courriers[] = $courriers;

        return $this;
    }

    /**
     * Remove courriers
     *
     * @param \AppBundle\Entity\Courrier $courriers
     */
    public function removeCourrier(\AppBundle\Entity\Courrier $courriers)
    {
        $this->courriers->removeElement($courriers);
    }

    /**
     * Get courriers
     *
     * @return \Doctrine\Common\Collections\Collection
     */
    public function getCourriers()
    {
        return $this->courriers;
    }

    /**
     * Set description
     *
     * @param string $description
     * @return Categorie
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }
}
